package cmpe275eat.takeoutapp;

class OrderItem {
    private int id;
    private int qty;

    public OrderItem() {

    }


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }
}
